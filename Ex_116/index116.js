

function getHexaColor (color) {

  
  if (color=='white'){
      return '#FFFFFF';
  }  if (color== 'black'){
      return '#000000';
  } if (color=='blue'){
      return '#0b24fb';
  } if (color=='green'){
      return ' #0e7e12';

  } if (color=='yellow'){
      return '#fffd38';

  } if (color=='pink'){
      return '#fec1cc';
  }

}



